						<h3 class="text-center">The History of Taekwondo</h3>

						<p>Early Koreans developed unique martial art forms for unarmed self defense to complement their skills with weapons. The first recorded evidence of what was to become modern Taekwondo is found about two thousand years ago in Korean history. A mural painting from the Koguryu kingdom (37 B.C to 66 A.D.) was found in a tomb believed to have been built sometime during the period 3 to 427 A.D . This mural depicts figures practicing martial arts techniques. Historical records from this Koguryu period also mention the practice of martial arts techniques and tournaments. The early forms had different names, such as Kwonbak, Bakhi, Dangsoo, Taesoo and Kongsoo.</p>

	        			<p>From about 600 A.D. to about 1400, the mainstream dominant form was Soobak, which further evolved into Taekyon beginning in the late 1300s. Taekyon was the dominant Korean martial art form until the Japanese invasion and occupation of Korea in 1909. From 1909 to 1945, the Japanese suppressed Korean culture and martial arts, and introduced Japanese culture and martial arts.</p>
	        			
	        			<p>The modern period of Taekwondo began with the defeat of the Japanese and the liberation of Korea in 1945. Korean martial arts masters wanted to eliminate Japanese influences. They began discussions on how to return to the traditional Taekyon based Korean martial arts and on how to unite the various martial arts schools (or Kwans) and styles into a single style and national sport. After several years of discussions, the name <em>Taekwondo</em> was chosen in April 1955 by the board of masters of the various Kwans, and the kwans started to unify through the late 1950s.</p>
				        
				        <p>The spread of Taekwondo as a martial art and competitive sport continues to this date. The principle events in the rapid evolution of Taekwondo as a popular world wide sport are:</p>
				        
				        <ul>
					        <li>1965 - The Korea Taekwondo Association was formed.</li>
					        <li>1973 - World Taekwondo Federation created.</li>
					        <li>1975 - General Association of International Sports Federations recognizes the WTF.</li>			        
					        <li>1976 - Taekwondo accepted as a Consul International du Sport Militaire sport (world level military sports organization).</li>
					        <li>1980 - International Olympic Committee recognizes the WTF.</li>
					        <li>1981 - Taekwondo accepted as a World Games sport</li>
					        <li>1983 - Taekwondo accepted as a Pan American Games and All Africa Games sport</li>
					        <li>1985 - Taekwondo adopted as a demonstration sport for the 1988 Olympic Games.</li>
					        <li>1986 - Taekwondo accepted as a Federation International du Sport Universitaire sport (world university level sport organization).</li>
					        <li>1992 - Taekwondo is an Olympic demonstration sport in Barcelona, Spain.</li>
					        <li>1994 - Taekwondo selected as a full Olympic sport for the 2000 Olympic Games in Sydney, Australia.</li>
					        <li>1996 - Taekwondo is an Olympic demonstration sport in Atlanta.</li>
					      	<li>Today - Taekwondo is by far the most widely practiced martial art in the world..</li>
